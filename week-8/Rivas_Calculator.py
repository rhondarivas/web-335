#this adds and returns sum of 2 numbers
def add(num1, num2):
    return(num1 + num2)

print(add(1, 2))

#this subtracts one number from another & returns the value
def subtract(num3, num4):
    return(num3-num4)

print(subtract(4,1))

#this divides one number by another & returns the value
def divide(num5, num6):
    return(num5/num6)

print(divide(8,2))
